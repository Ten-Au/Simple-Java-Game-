package Grid;


public class Grid {
    private final int gridX;
    private final int gridY;

    public Grid(int gridX, int gridY) {
        this.gridX = gridX;
        this.gridY = gridY;
    }



    public int getGridX() {
        return gridX;
    }

    public int getGridY() {
        return gridY;
    }
}
